# single line strings
a = 'namita'
b = "aman"
print(a)
print(b)

# multi line strings
c = """Hey, 
I am Peyush"""
print(c)

# mix single and double quotes for using them in strings respectively
line1 = "I'am good"
line2 = 'I"am good'
print(line1)
print(line2)

# indexing
text = "python"
print(text[0])

for char in text:
    print(char)
